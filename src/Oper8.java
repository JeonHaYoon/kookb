public class Oper8 {
	public static void main(String[] args) {
		int i = 0;
		switch (i) {
		case 1:
			System.out.println("로그인 호출");
			break;

		case 2:
			System.out.println("쪽지확인");
			break;

		case 0:
			System.out.println("종료.");
			break;

		default:
			System.out.println("잘못입력하셨습니다.");
		}

	}
}

