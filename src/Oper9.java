public class Oper9 {

	public static void main(String[] args) {
	
		int myJumsu=75;
		
		if(myJumsu>=90)
			System.out.println("A");
		else if(myJumsu>=80)
			System.out.println("B");
		else if(myJumsu>=70)
			System.out.println("C");
		else if(myJumsu>=60)
			System.out.println("D");
		else System.out.println("F");

		
	}

}
